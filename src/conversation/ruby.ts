// 青空文庫式ルビの解決(Claude振り仮名方式・B-06 の前提)。
//
// Claude は message に「読みが文脈で割れる語・難読の固有名詞だけ」へ青空文庫式ルビを付ける:
//   - 漢字《よみ》         … 直前の漢字連に よみ(ひらがな)を当てる
//   - ｜任意の基底《よみ》  … 基底の区切りが曖昧なとき ｜(全角縦棒)で基底の先頭を明示
//
// ここから2つを導出する:
//   - 表示(吹き出し): stripRuby … 《よみ》と ｜ を除去し、漢字はそのまま見せる
//   - 音声(audio_query): rubyToReading … 基底《よみ》→ よみ に置換、それ以外はエンジン辞書に任せる
//
// 例:「最近、夏目漱石の心《こころ》を読みました。向上心《こうじょうしん》は大切だと思いました。」
//   表示 → 「最近、夏目漱石の心を読みました。向上心は大切だと思いました。」
//   音声 → 「最近、夏目漱石のこころを読みました。こうじょうしんは大切だと思いました。」
//   (同じ「心」が こころ / こうじょうしん に文脈で読み分かる)

/** 表示用テキスト:ルビ(《よみ》)と基底マーカ(｜)を取り除き、漢字はそのまま残す。 */
export function stripRuby(message: string): string {
  return message.replace(/《[^《》]*》/g, '').replace(/｜/g, '');
}

/**
 * 音声用テキスト:基底《よみ》を よみ に置換する(それ以外の語はエンジンの辞書読みに委ねる)。
 * 取り残した《よみ》は安全に除去する(ブラケットを読み上げない・基底は残置)。
 */
export function rubyToReading(message: string): string {
  return message
    .replace(/｜([^｜《》]+)《([^《》]*)》/g, '$2') // ｜基底《よみ》 → よみ(基底が曖昧/非漢字のとき)
    // 漢字＋数字＋英字＋% の連(=注釈対象の語)《よみ》 → よみ。例「3冊」「100%」「向上心」「AI」。
    // 直前のひらがな・カタカナ・句読点は基底に含めない(そこで切れる)。
    .replace(/[0-9０-９A-Za-zＡ-Ｚａ-ｚ%％㐀-䶿一-鿿々〆〇ヶ]+《([^《》]*)》/g, '$1')
    .replace(/《[^《》]*》/g, '') // 取り残し《よみ》は除去(基底はそのまま=エンジン辞書に委ねる)
    .replace(/｜/g, '');
}

/** ルビ注釈(《…》）を含むか。 */
export function hasRuby(message: string): boolean {
  return /《[^《》]*》/.test(message);
}
